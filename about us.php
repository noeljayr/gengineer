    <div class="page-title">
      <h1>About Us</h1>
    </div>
    <div class="main-content page-section">
      <div class="info">
        <p>
          Girl Engineer is a Malawi-based registered non-profit organization
          that is dedicated to creating opportunities that inspire, educate, and
          empower young girls to pursue STEM (Science, Technology, Engineering,
          and Mathematics) fields of study and careers. Our organization firmly
          believes in the immense potential of young girls to achieve greatness
          in STEM fields, and we work tirelessly to create opportunities that
          allow them to reach their full potential.
        </p>
      </div>

      <div class="info">
        <p>
          Our primary focus is on designing and delivering hands-on STEM
          outreach workshops to primary schools. We provide interactive learning
          experiences to increase STEM awareness and encourage girls to explore
          the possibilities of these exciting fields. We are convinced that a
          solid foundation in STEM education will not only benefit individual
          girls but also contribute to the development of Malawi's economy and
          society as a whole.
        </p>
      </div>

      <div class="info">
        <p>
          In addition to our hands-on STEM outreach workshops, we offer
          complementary programs such as STEM Tours, STEM Role Models, STEM
          Awareness, and STEM Sponsorships. Detailed information about these
          programs can be found on our homepage in the "Our Programs" section.
        </p>
      </div>

      <div class="info">
        <p>
          All our programs are interconnected in a meaningful way. We initiate
          young girls' interest and curiosity in STEM fields through our STEM
          workshops. Subsequently, our STEM Role Models program exposes them to
          successful women and girls who have thrived in STEM, serving as
          inspirational figures. Through our STEM tours, we provide real-world
          examples of STEM applications and job opportunities. Our STEM
          Sponsorship program takes a step further by providing financial
          support to girls facing economic challenges, enabling them to continue
          their STEM education. Finally, our STEM awareness campaigns educate
          parents and guardians on how to equally encourage and support their
          daughters in pursuing STEM, just as they would their sons.
        </p>
      </div>

      <div class="info">
        <h2 style="text-align: center">
          M’mera Mpoyamba | Early Moments Matter
        </h2>
        <p>
          STEM programs designed to ignite young girls' curiosity in Science,
          Technology, Engineering, and Mathematics may not always yield
          immediate, quantifiable results. However, the imperative to support
          these initiatives lies in their capacity to sow the seeds for a more
          inclusive, innovative, and skilled STEM community. Here's why we
          should wholeheartedly endorse early girls' STEM programs:
        </p>
      </div>

      <div class="info">
        <div>
          <span class="number">1.</span>
          <p>
            <span>Long-Term Impact:</span>
            While the fruits of these programs may be difficult to gauge
            immediately, the enduring impact is undeniable. We are nurturing a
            generation of girls who will mature into STEM leaders, infusing the
            landscape with fresh perspectives, creative thinking, and a
            commitment to inclusivity.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
            <span>Learning and Skill Development:</span>
            These programs equip girls with invaluable skills such as
            problem-solving, critical thinking, technical proficiency, and the
            ability to collaborate effectively. These skills are indispensable,
            irrespective of immediate, measurable outcomes.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">3.</span>
          <p>
            <span>Nurturing Curiosity: </span>
            By supporting girls in their STEM pursuits from an early age, we are
            stoking the flames of their innate curiosity. This early nurturing
            can kindle a lifelong passion for STEM, one that might eventually
            lead to groundbreaking discoveries and innovations.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">4.</span>
          <p>
            <span>Confidence Building: </span>
            Participation in STEM programs can serve as a catalyst for
            bolstering girls' confidence and self-esteem. Even if a program
            doesn't yield quantifiable outcomes in the short term, the process
            of exploration and experimentation is invaluable for personal
            growth.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">5.</span>
          <p>
            <span>Promoting Inclusivity: </span>
            Funding girls in STEM programs sends an unequivocal message that
            STEM is a realm open to all, regardless of gender. This resonates as
            a call for a more diverse and representative STEM community, one
            that enriches all stakeholders in the long run.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">6.</span>
          <p>
            <span>Community and Collaboration: </span>
            STEM programs frequently entail collaboration and teamwork, teaching
            girls essential social and collaborative skills. These abilities are
            readily transferable to various aspects of life, underscoring the
            holistic benefits of early engagement in such programs.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">7.</span>
          <p>
            <span>Long-Term Societal Benefits: </span>
            While immediate, measurable outcomes hold their significance, it's
            imperative to recognize the broader, enduring societal gains derived
            from early investments in girls' STEM programs. A more diverse and
            highly skilled STEM workforce emerges, contributing to the
            collective advancement of society.
          </p>
        </div>
      </div>

      <div class="info">
        <h2 style="text-align: center">Girl Engineer’s Mission</h2>
        <p>
          <span>Our mission </span>
          at Girl Engineer is to create opportunities that inspire, educate, and
          empower young girls to pursue STEM fields of study and careers. We
          firmly believe that every girl has the potential to make a meaningful
          contribution to the world of STEM and that they deserve the
          opportunity to explore these fields in a supportive and empowering
          environment.
        </p>
      </div>

      <div class="info">
        <h2 style="text-align: center">Girl Engineer’s Vision</h2>
        <p>
          <span>Our vision </span>
          is to close the gender gap in STEM fields of study and careers. We
          strive to create a future where girls have equal access to STEM
          education, resources, and opportunities, and where they can fulfil
          their potential and achieve their dreams.
        </p>
      </div>
      
      <div class="info">
        <h2 style="text-align: center">Girl Engineer’s Core Values</h2>
      </div>

      <div class="info">
        <div>
          <span class="number">1.</span>
          <p>
            <span>Empowerment:</span>
            Girl Engineer believes in empowering young girls to reach their full
            potential in STEM fields. We provide them with the resources,
            support, and opportunities they need to succeed.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
            <span>Inspiration:</span>
            We are passionate about inspiring young girls to explore the world
            of STEM and pursue their dreams. We believe that every girl has the
            potential to make a meaningful contribution to society.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">3.</span>
          <p>
            <span>Inclusivity:</span>
            We are committed to creating an inclusive and equitable environment
            for all girls. We strive to break down barriers and ensure that
            every girl has the opportunity to succeed in STEM.
          </p>
        </div>
      </div>
      
            <div class="info">
        <div>
          <span class="number">4.</span>
          <p>
            <span>Innovation:</span>
            We are dedicated to fostering creativity and innovation in STEM education.
We encourage girls to think critically and solve problems in new and creative ways.
          </p>
        </div>
      </div>
      
            <div class="info">
        <div>
          <span class="number">5.</span>
          <p>
            <span>Impact:</span>
           We believe in making a lasting impact on the lives of young girls in Malawi. We
are committed to helping them reach their full potential and become leaders in STEM
          </p>
        </div>
      </div>

      <div class="info">
        <p>
          We are committed to making a positive difference in the lives of young
          girls in Malawi, and we invite you to join us on this journey.
          Together, we can inspire the next generation of female leaders in STEM
          and create a more equitable and prosperous world for all.
        </p>
      </div>
    </div>

