    <div class="page-title">
      <h1>Volunteer</h1>
    </div>
    <div class="main-content page-section">
      <p>
        At Girl Engineer, we offer exciting and rewarding volunteer
        opportunities for individuals who are passionate about empowering young
        girls and promoting STEM education. Join us in making a difference in
        the lives of girls and inspiring them to pursue their dreams. Here are
        two key volunteer roles:
      </p>

      <div class="info">
        <div>
          <span class="number">1.</span>
          <p>
          <span>Proposal Writer : </span>
          Are you skilled in grant writing and proposal development? As a
          volunteer proposal writer, you will play a crucial role in helping us
          secure funding for our programs and initiatives. Your excellent
          writing skills and ability to craft persuasive proposals will
          contribute to the sustainability and growth of our organization.
        </p>
        </div>
        
      </div>
      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
          <span>STEM Workshop Instructor :</span>
          Are you enthusiastic about STEM subjects and love sharing your
          knowledge with others? As a volunteer STEM workshop instructor, you
          will have the opportunity to inspire and educate young girls in the
          fields of Science, Technology, Engineering, and Mathematics. Through
          hands-on activities and engaging lessons, you will help spark
          curiosity and passion for STEM among our participants.
        </p>
        </div>
        
      </div>
      <div class="info">
        <div>
          <span class="number">3.</span>
          <p>
          <span>Other Opportunities:</span>
          While our current focus is on proposal writing and STEM workshop
          instruction, we occasionally have other volunteer positions available.
          These may include event coordination, fundraising support, graphic
          design, content creation, or administrative tasks. Stay tuned for
          updates on additional volunteer opportunities.
        </p>
        </div>
        
      </div>

      <div class="info">
        <h2 style="text-align: center">Benefits of Volunteering</h2>

        <p>
          Volunteering with Girl Engineer is a rewarding experience that offers
          numerous benefits, including:
        </p>
      </div>

      <div class="info">
        <div>
          <span class="number">1.</span>
          <p>
          <span>Making a Positive Impact:</span>
          Your contribution will directly impact the lives of young girls,
          inspiring them to pursue STEM education and opening doors to exciting
          career opportunities.
        </p>
        </div>
        
      </div>
      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
          <span>Professional Development:</span>
          Gain valuable experience and enhance your skills in areas such as
          proposal writing, teaching, event coordination, or administration.
        </p>
        </div>
        
      </div>

      <div class="info">
        <div>
          <span class="number">3.</span>
          <p>
          <span>Networking Opportunities:</span>
          Connect with like-minded individuals, professionals, and organizations
          in the STEM and education sectors, expanding your network and
          potential career opportunities.
        </p>
        </div>
        
      </div>

      <div class="info">
        <div>
          <span class="number">4.</span>
          <p>
          <span>Personal Fulfilment:</span>
          Experience the joy of making a difference in the lives of others,
          witnessing the growth and success of the girls you inspire.
        </p>
        </div>
        
      </div>

      <div class="info">
        <h2 style="text-align: center">Volunteer Requirements</h2>

        <p>
          To become a volunteer with Girl Engineer, we ask that you meet the
          following requirements:
        </p>
      </div>

      <div class="info">
        <div>
          <span class="number">1.</span>
          <p>
          <span>Passion for our Mission: </span>
          Share our commitment to inspiring, educating, and empowering young
          girls to pursue STEM fields.
        </p>
        </div>
        
      </div>

      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
          <span>Relevant Skills and Experience:</span>
          Depending on the volunteer role, certain skills or experience may be
          required. For example, proposal writers should have excellent writing
          and communication skills, while workshop instructors should have a
          strong background in STEM subjects.
        </p>
        </div>
        
      </div>

      <div class="info">
        <div>
          <span class="number">3.</span>
          <p>
          <span>Time Commitment:</span>
          Be willing to dedicate a specific amount of time per week or month,
          depending on the volunteer role and project.
        </p> 
        </div>
        
      </div>

      <div class="info">
        <h2 style="text-align: center">How to Become a Volunteer</h2>

        <p>
          If you are excited about volunteering with Girl Engineer and making a
          positive impact, follow these steps to get started:
        </p>
      </div>

      <div class="info">
        <div>
          <span class="number">1.</span>
          <p>
            <span>Review Available Opportunities:</span>
            Take a closer look at the volunteer opportunities mentioned above
            and determine which role aligns best with your skills and interests.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
            <span>Contact Us:</span>
            Reach out to us via email at info@gengineer.org to express your
            interest in volunteering. Please mention the specific volunteer role
            you are interested in.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">3.</span>
          <p>
            <span>Application and Interview: </span>
            We will respond to your email and provide you with any necessary
            application forms or further instructions. We may also schedule an
            interview to discuss your qualifications, interests, and
            availability.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">4.</span>
          <p>
            <span>Orientation and Training:</span>
            Once selected as a volunteer, you will receive an orientation
            session to familiarize yourself with our organization, programs, and
            policies. If necessary, we will provide training specific to your
            volunteer role.
            <br />
            <br />
            We look forward to welcoming you to our team of dedicated
            volunteers!
          </p>
        </div>
      </div>

      <div class="info">
        
        <h2 style="text-align: center">Contact Information</h2>

        If you have any questions or require further information about
        volunteering with Girl Engineer, please feel free to reach out to us at
        <a href="mailto:info@gengineer.org">info@gengineer.org</a>. We appreciate your interest and
        commitment to empowering young girls in STEM fields.

        <br />
        <br />
        Thank you for considering Girl Engineer as your volunteering opportunity
        of choice!
      </div>
    </div>

