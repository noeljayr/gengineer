    <div class="page-title">
      <h1>Partner With Us</h1>
    </div>
    <div class="main-content page-section">
      <div class="info">
        <p>
          At Girl Engineer, we are on a mission to inspire, educate, and empower
          young girls in STEM fields, and we believe that partnerships play a
          crucial role in achieving this vision. We invite organizations to join
          hands with us and become a part of our mission, benefiting from a
          range of exciting partnership opportunities while making a meaningful
          impact on the lives of girls and contributing to a more diverse and
          inclusive future in STEM.
        </p>
      </div>

      <div style=" margin-bottom: 0" class="info">
        <h2 style="text-align: center;">Partnership Benefits</h2>
      </div>

      <div style="margin: 0;" class="info">
        <div>
          <span class="number">1.</span>
          <p>
            <span>Brand Exposure and Online Recognition:</span>
            Partnering with us provides organizations with a powerful platform
            to not only showcase their brand but also to increase their online
            visibility and credibility. Your brand will receive prominent
            recognition in our "Our Partners" section on our website, accompanied 
            by the prominent display of your logo. This not only enhances 
            your online presence but also signifies a sincere
            commitment to the cause of gender equality in STEM. It's an
            opportunity to connect with a diverse and engaged audience, forging
            a deeper connection with potential customers who share our passion
            for this vital mission. This association can substantially
            strengthen your brand's image and foster trust and affection among
            your target audience.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
            <span>Driving Social Impact:</span>
            When your organization partners with Girl Engineer, you become a 
            catalyst for profound social change within the dynamic landscape of 
            STEM fields. Your support directly fuels efforts to narrow the gender 
            gap in STEM, actively empowering young girls to chart paths in these 
            critical areas. By dismantling barriers and providing opportunities, you 
            play a pivotal role in helping these girls overcome historical obstacles 
            that have traditionally hindered their success in STEM studies and careers. 
            This partnership serves as a tangible manifestation of your organization's 
            commitment to both social responsibility and gender equality within the realms 
            of Science, Technology, Engineering, and Mathematics. It is not just an affiliation; 
            it's an invitation to actively participate in shaping a brighter, more inclusive 
            future where gender equality thrives in every facet of STEM education and careers.
          </p>
        </div>
      </div>

      <div class="info">
        <h2 style="text-align: center">Partnership Opportunities</h2>
        <p>
          At Girl Engineer, we offer a spectrum of partnership opportunities
          that enable organizations to actively participate in our mission of
          inspiring, educating, and empowering young girls in STEM fields. By
          partnering with us, you have the chance to make a substantial impact
          on the lives of girls while contributing to a more diverse and
          inclusive future in STEM.
        </p>
      </div>

      <div class="info">
        <div>
          <span class="number">1.</span>
          <p>
            <span>STEM Tours:</span>
            Partners can collaborate with us to organize STEM tours, a dynamic
            way to showcase their companies or organizations. These tours
            provide young girls with an exclusive window into the world of STEM
            by allowing them to visit your facilities. During these visits, they
            can engage with professionals in STEM fields, witness real-world
            applications of STEM subjects, and gain insights into various career
            paths. Your organization's support in arranging these tours and
            covering related expenses leaves a lasting impression on the girls,
            helping them bridge the gap between classroom learning and practical
            experiences.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
            <span>STEM Awareness:</span>
            Our STEM awareness campaign is a pivotal initiative aimed at
            creating a more inclusive environment for girls in STEM. As a
            partner, you can join us in spreading awareness about the importance
            of gender equality in STEM and highlighting the remarkable
            contributions of women in these fields. Together, we can organize a
            wide range of events, workshops, seminars, and panel discussions
            that foster dialogue, challenge stereotypes, and promote diversity
            and inclusivity. By championing these messages, we aim to inspire
            young girls to pursue STEM education and careers while encouraging
            parents, educators, and the broader community to offer their
            unwavering support.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">3.</span>
          <p>
            <span>STEM Role Models: </span>
            Representation is crucial, and our STEM Role Models program offers
            partners the opportunity to provide female professionals who can
            serve as mentors and role models for young girls. Your organization
            can play a pivotal role by arranging guest speaker sessions where
            accomplished women in STEM share their experiences, challenges, and
            achievements. Through these sessions, these role models inspire and
            empower girls, illustrating that they too can thrive in STEM
            careers.
          </p>
        </div>
      </div>

      <div class="info">
        <h2 style="text-align: center">How to Become a Partner</h2>
        <p>
          If you are interested in partnering with Girl Engineer and exploring these exciting
opportunities in further detail, please reach out to us at
          <a href="mailto:info@gengineer.org">info@gengineer.org</a>. We are eager to
hear from you and look forward to working together to create a brighter future for young girls
in STEM. Thank you for your interest in partnering with us! Together, we can make a
significant impact and inspire the next generation of female STEM leaders.
        </p>
      </div>
    </div>

