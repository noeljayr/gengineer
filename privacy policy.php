        <h2>Do we use cookies and other tracking technologies?</h2>
        <p>
          We do not use cookies and similar tracking technologies.
        </p>
      </div>

      <div class="info">
        <h2>Controls for do-not-track features</h2>
        <p>
          Most web browsers and some mobile operating systems and mobile
          applications include a Do-Not-Track ("DNT") feature or setting that
          you can activate to signal your privacy preference not to have data
          about your online browsing activities monitored and collected. At this
          time, no uniform technology standard for recognizing and implementing
          DNT signals has been finalized. As such, we do not currently respond
          to DNT browser signals or any other mechanism that automatically
          communicates your choice not to be tracked online. If a standard for
          online tracking is adopted that we must follow in the future, we will
          inform you about that practice in a revised version of this privacy
          notice.
        </p>
      </div>

