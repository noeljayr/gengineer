      <div class="info">
        <h2 style="text-align: center">Sponsorship Opportunities</h2>
        <p>
          We offer a range of sponsorship opportunities that allow organizations
          to actively participate in our mission of inspiring, educating, and
          empowering young girls in STEM fields. By sponsoring us, you can make
          a meaningful impact on the lives of girls and contribute to a more
          diverse and inclusive future in STEM.
        </p>
      </div>

      <div class="info">
        <div>
          <span class="number">1.</span>
          <p>
            <span>STEM Workshops: </span>
            Sponsor our STEM workshops, the cornerstone of our outreach efforts.
            Your recurring donations or resource contributions will enable us to
            deliver engaging, hands-on STEM activities to girls in primary
            schools. These workshops not only impart valuable learning
            experiences but also nurture curiosity, critical thinking,
            problem-solving skills, and a deep passion for STEM subjects.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">2.</span>
          <p>
            <span>STEM Tours: </span>
            Consider financial sponsorship for our STEM Tours, supporting
            essential elements such as transportation and necessary resources.
            These tours offer girls a firsthand experience of real-world STEM
            applications. During the tours, girls visit companies, interact with
            STEM professionals, and gain insights into diverse STEM career
            paths. Your organization's sponsorship will have a lasting impact,
            bridging the gap between classroom learning and practical
            experiences.
          </p>
        </div>
      </div>

      <div class="info">
        <div>
          <span class="number">3.</span>
          <p>
            <span>STEM Sponsorship: </span>
            Our STEM Sponsorship program seeks to remove financial barriers that
            hinder girls' access to STEM education. As a sponsor, you can make a
            profound difference in the lives of deserving girls by providing
            essential financial support. Your sponsorship can cover school fees,
            textbooks, educational materials, and other critical resources. By
            enabling girls from disadvantaged backgrounds to pursue STEM
            education, you help them unlock their potential, creating a brighter
            future for themselves and their communities.
          </p>
        </div>
      </div>

