    <div style="margin-top: 3rem;" class="main-content section">
      <p>
        Our STEM Awareness program is an outreach initiative designed to engage 
        and inform parents, guardians, and the broader community about the importance
        of STEM (Science, Technology, Engineering, and Mathematics) education and 
        careers for girls. By working together, we can create a supportive environment
        that empowers girls to explore and excel in these fields.
        <br /><br />
        We recognize that STEM fields have historically been associated with masculinity, 
        leading to a significant underrepresentation of women. Unfortunately, these
        stereotypes continue to persist, and even teachers, parents, guardians, and
        the community at large may underestimate the abilities of girls in STEM. It's 
        crucial to raise awareness that girls and women are just as capable in STEM
        fields of study and careers as boys and men.
        <br /><br />
        To combat this gender stereotype, we conduct informative sessions and workshops 
        for parents, guardians, and community members to highlight the significance of
        STEM education for girls. Provide insights into the diverse career opportunities
        available in STEM fields and the potential impact on the future workforce.
        <br /><br />
        We also invite successful women working in STEM professions to share their
        experiences and journeys with parents and the community. By showcasing real-life 
        examples of girls who have excelled in STEM, we emphasize the positive outcomes
        of pursuing STEM education and provide relatable role models for girls to look up to.
        <br /><br />
        The campaign also emphasizes the importance of early exposure to STEM education 
        and the significance of building a solid foundation in STEM subjects from a 
        young age. We encourage parents and guardians to encourage their daughters to
        participate in our STEM Workshop program, where they can explore their interests,
        develop their skills, and build confidence in their abilities.
      </p>

