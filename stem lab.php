      <div class="info">
        <p>
          At Girl Engineer, our unwavering commitment lies in creating
          opportunities that inspire, educate, and empower young girls to pursue
          careers in STEM (Science, Technology, Engineering, and Mathematics).
          We achieve this by meticulously designing and delivering hands-on STEM
          workshops in primary schools. However, a significant challenge we face
          is that many of the primary schools we serve lack suitable spaces for
          our interactive STEM workshops.
          <br /><br />
          To overcome this obstacle, we have conceived an innovative solution—a 
          Mobile STEM Lab. This remarkable initiative involves transforming a bus's 
          interior into a fully equipped learning environment, complete with tables 
          and chairs. The Mobile STEM Lab will not only overcome the challenge of 
          lack of proper space for our hands-on STEM workshops, but it will also 
          attract more girls to participate in these STEM workshops.
          <br /><br />
          By bringing this mobile laboratory to underserved schools, we can offer 
          quality STEM education to girls who may otherwise miss out on these valuable 
          opportunities. We believe that the Mobile STEM Lab will be a game-changer 
          for girls in Malawi, providing them with the chance to explore their interests 
          in STEM and develop the skills and confidence needed to succeed in these fields.
          <br /><br />
          At present, the Mobile STEM Lab is a dream that Girl Engineer is
          tirelessly working to turn into reality. Our mission hinges on
          fundraising to bring this project to fruition. Our ultimate goal is to
          extend the reach of our STEM workshops to more girls, helping them
          develop the skills and confidence needed to excel in STEM fields.
          <br /><br />
          At Girl Engineer, we are resolute in our mission to make a meaningful
          impact on the lives of young girls in Malawi. With your invaluable
          support, we can break down barriers, provide more girls with access to
          STEM education, and collectively create a brighter and more inclusive
          future for all.
          
        </p>
