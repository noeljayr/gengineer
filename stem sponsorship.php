    <div style="margin-top: 3rem;" class="main-content section">
      <p>
        Girl Engineer's STEM Sponsorship is a dedicated support program designed 
        to offer financial assistance to underprivileged primary school girls with 
        a passion for STEM fields. Our mission at Girl Engineer is rooted in the
        belief that every girl, regardless of her economic background, should have
        an equal opportunity to thrive in STEM.
        <br /><br />
        Through our STEM Sponsorship program, we provide comprehensive support to
        girls facing financial challenges, ensuring they have the resources needed
        to pursue their interest in STEM. This support includes covering school fees, 
        providing school uniforms, and supplying essential writing materials. By 
        addressing these financial barriers, we aim to prevent the girls from the 
        risk of dropping out of school, enabling them to stay focused on their
        educational journey.
        <br /><br />
        To ensure the effectiveness of our program, we closely monitor the participants
        of our STEM workshops. This proactive approach allows us to identify and address
        any financial constraints promptly, ensuring that these girls can continue their 
        education and pursue their dreams of entering STEM careers.
        <br /><br />
        In addition to financial support, we actively seek out STEM-related scholarships
        to further empower these aspiring girls. We are confident that this initiative
        will bring about meaningful change in the lives of young girls who are not only 
        passionate about STEM but also unwavering in their determination to 
        achieve their dreams.
        <br /><br />
        At Girl Engineer, we are committed to breaking down financial barriers and 
        creating a pathway for underprivileged girls to excel in STEM, fostering a
        future generation of diverse and talented women in these fields.
      </p>

