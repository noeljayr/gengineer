    <div style="margin-top: 3rem;" class="main-content section">
      <p>
        Our STEM Workshops are engaging, and educational hands-on outreach activities
        designed to spark curiosity and excitement in fields of Science, Technology, 
        Engineering, and Mathematics (STEM) in young minds, specifically primary 
        school girls.
        <br /><br />
        Our carefully curated workshops cover a diverse range of topics, including 
        Renewable Energy, Stop Motion Animation, Introduction to Coding, Structural 
        Engineering, Electric Propeller Cars, Electricity and Magnetism, and much more. 
        You can explore detailed information about each workshop, including descriptions, 
        learning outcomes, target age groups, and watch informative videos on 
        “Our STEM Workshops” section of our homepage.
        <br /><br />
        Designing and delivering these engaging, hands-on STEM outreach workshops 
        in primary schools is our passion and primary focus. Our mission is to empower
        young girls with the skills and knowledge they need to excel in the fields of STEM. 
        We are committed to sparking curiosity and fostering interest in STEM subjects 
        while introducing girls to a wide array of exciting career possibilities.
        <br /><br />
        Our workshops are more than just inspiration – they're a launching pad 
        for young girls to become the next generation of STEM leaders. We equip 
        them with the skills and knowledge they need to thrive in STEM fields.
        <br /><br />
        We are continually expanding our workshop offerings to include new and
        captivating topics, such as Coding with Robots. This STEM activity will
        introduce girls to the fascinating world of robotics, illustrating how
        coding plays a pivotal role in this field.
        <br /><br />
      </p>

